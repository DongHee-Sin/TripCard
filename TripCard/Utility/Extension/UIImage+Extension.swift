import UIKit


extension UIImage {
    
    static var locationImage: UIImage? {
        return UIImage(systemName: "location.circle")
    }
    
    static var calendarImage: UIImage? {
        return UIImage(systemName: "calendar")
    }
    
    static var writeImage: UIImage? {
        return UIImage(systemName: "square.and.pencil")
    }
    
    static var xmarkImage: UIImage? {
        return UIImage(systemName: "xmark")
    }
    
}
